import 'package:firebase_analytics/firebase_analytics.dart';
import 'package:flutter/material.dart';
import 'package:responsive_grid/responsive_grid.dart';
import 'package:url_launcher/url_launcher.dart';

class StudentAccommodation extends StatelessWidget {
  UrlHandler urlHandler = UrlHandler();

  @override
  Widget build(BuildContext context) {
    return SingleChildScrollView(
      child: Column(mainAxisSize: MainAxisSize.max, children: [
        Row(
          children: [
            Text(
              "Student Accommodation in Australia",
              overflow: TextOverflow.visible,
              style: const TextStyle(
                  fontSize: 30, color: Color.fromARGB(255, 37, 74, 118)),
            ),
            Padding(
              padding: EdgeInsets.only(left: 20, right: 10),
              child: Icon(
                Icons.house,
                color: Color.fromARGB(255, 37, 74, 118),
              ), // icon is 48px widget.
            )
          ],
        ),
        Row(
          children: [
            Expanded(
              child: Padding(
                padding: EdgeInsets.only(top: 10, bottom: 10),
                child: Text(
                  "Purpose-built student accommodation either on-campus or within walking distance of your school",
                  overflow: TextOverflow.visible,
                  style: const TextStyle(
                      fontSize: 18, color: Color.fromARGB(255, 37, 74, 118)),
                ),
              ),
            ),
          ],
        ),
        ResponsiveGridRow(
          children: [
            buildResponsiveCardHyperlink(
                "Campus Living Villages",
                "Campus Living Villages has a proud history of setting new standards for student living, learning and lifestyle experiences",
                "https://campuslivingvillages.com/australia",
                "assets/campus_living_villages.jpg",
                BoxFit.fitWidth
            ),
            buildResponsiveCardHyperlink(
                "Uni Lodge",
                "Top Student Housing in Australia and New Zealand",
                "https://www.unilodge.com.au/",
                "assets/unilodge.png",
                BoxFit.fitWidth
            ),
            buildResponsiveCardHyperlink(
                "Urbanest Australia",
                "Urbanest Student Accommodation offers quality accommodation for students in Australia",
                "https://urbanest.com.au/",
                "assets/urbanest.png",
                BoxFit.fitWidth
            ),
            buildResponsiveCardHyperlink(
                "Student.com",
                "Book student housing with Student.com, the world's largest marketplace for international student housing.",
                "https://www.student.com/",
                "assets/student.png",
                BoxFit.fitWidth
            ),
          ],
        ),
      ]),
    );
  }

  ResponsiveGridCol buildResponsiveCardHyperlink(String title, String body, String link, String img, BoxFit boxfit) {
    return ResponsiveGridCol(
      xs: 20,
      md: 3,
      child: Container(
        height: 200,
        child: Card(
          elevation: 5,
          clipBehavior: Clip.antiAlias,
          child: InkWell(
            onTap: () {
              FirebaseAnalytics().logEvent(name: title,parameters:null);
              urlHandler.launchURL(link);
            },
            child: ListTile(
              contentPadding: EdgeInsets.only(left: 20, right: 10, top: 20),
              title: Text(title),
              subtitle: Text(body),
              trailing: Image.asset(
                img, fit: boxfit,
                width: 100, height: 100,
              ),
            ),
          ),
        ),
      ),
    );
  }
}

class UrlHandler {
  launchURL(String url) async {
    if (await canLaunch(url)) {
      await launch(url);
    } else {
      throw 'Could not launch $url';
    }
  }
}
